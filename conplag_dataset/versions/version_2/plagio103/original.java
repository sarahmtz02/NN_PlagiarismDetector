public class CondicionalMultiple {
    public static void main(String[] args) {
        int opcion = 2;
        if (opcion == 1) {
            System.out.println("Opción seleccionada: Uno");
        } else if (opcion == 2) {
            System.out.println("Opción seleccionada: Dos");
        } else {
            System.out.println("Opción no reconocida");
        }
    }
}