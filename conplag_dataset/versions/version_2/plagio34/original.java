public class CompararNumeros {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        if (a > b) {
            System.out.println("A es mayor que B");
        } else if (b > a) {
            System.out.println("B es mayor que A");
        } else {
            System.out.println("A y B son iguales");
        }
    }
}