public class EvaluacionOR {
    public static void main(String[] args) {
        boolean estadoA = false;
        boolean estadoB = true;
        if (estadoA) {
            System.out.println("El estado A es verdadero.");
        } else if (estadoB) {
            System.out.println("El estado B es verdadero.");
        } else {
            System.out.println("Ambos estados son falsos.");
        }
    }
}