public class RegresivaNumerica { // Renombrado de clase
    public static void main(String[] args) {
        int k = 5; // Uso de while
        while (k >= 1) { // Condición de parada y cambio de operador
            System.out.println("Número descendente: " + k); // Cambio de mensaje
            k = k - 1; // Decremento explícito
        }
    }
}