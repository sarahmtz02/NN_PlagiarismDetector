public class TransformarGrados { // Renombrado de clase
    public static void main(String[] args) {
        double gradosC = 25.0; // Renombrado de variable
        double gradosF = (gradosC * 1.8) + 32.0; // Uso de 1.8 en lugar de 9/5 y 32.0
        System.out.println(gradosC + " grados Celsius equivalen a " + gradosF + " grados Fahrenheit."); // Cambio de mensaje
    }
}