public class RestoDeDivision { // Renombrado de clase
    public static void main(String[] args) {
        int n = 17; // Renombrado de variable
        int d = 5; // Renombrado de variable
        int remanente = n - (n / d) * d; // Cálculo manual del residuo
        System.out.println("El remanente de la operación es: " + remanente); // Cambio de mensaje
    }
}