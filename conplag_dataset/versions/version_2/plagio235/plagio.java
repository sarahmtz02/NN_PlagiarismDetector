public class EncontrarValorMayor {
    public static void main(String[] args) {
        int val1 = 10;
        int val2 = 20;
        int mayor;
        if (val1 > val2) {
            mayor = val1;
        } else {
            mayor = val2;
        }
        System.out.println("El número más grande es: " + mayor);
    }
}